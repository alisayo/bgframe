 //左侧导航加载
 function tree(data, i) {
     i++;
     treeStr += '<ul>';
     if (typeof data == "object") {
         $.each(data, function(k, v) {
             if (v.url != null) {
                 treeStr += '<li class="ys-tree-level' + i + '"><a target="main" href="' + v.url + '">' + v.name + '</a></li>'
             } else {
                 if (i == 1)
                     treeStr += '<li class="ys-tree-level' + i + '"><h5><span class="' + v.icon + '"></span><span class="yc-tree-text">' + v.name + '</span><span class="ys-arrow glyphicon glyphicon-menu-down"></span></h5>  ';
                 else treeStr += '<li class="ys-tree-level' + i + '"><h6><span class="yc-tree-text ">' + v.name + '</span></h6>  ';
                 tree(v.menus, i);
                 treeStr += '</li>';
             }
         })
     }

     treeStr += '</ul>';
 }

